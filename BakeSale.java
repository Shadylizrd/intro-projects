public class BakeSale {
    public static void main(String[] args) {
        int numCookies = 17;

        // number of cookies after 3 are bought
        // note: you do not need to declare the variable type after declaration
        numCookies -= 3;
        // number of cookies after half of the remaining are bought
        numCookies /= 2;
        System.out.println(numCookies);
    }
}


