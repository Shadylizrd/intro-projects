// Task explaining how some of the primitive operators can work with strings (not all can)

public class Zoo {
    public static void main(String[] args) {
        int animals = 12;
        String species = "zebra";
        // Using the "+" operator to connect different variables to a string
        String zooDescription = "Our zoo has "+animals+" "+species+"s!";

        System.out.println(zooDescription);
    }
    
}
